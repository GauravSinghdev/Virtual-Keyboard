function clean()
{
    document.getElementById("txt").innerHTML = "";
}

function backspace()
{
    let result = document.getElementById("txt").innerHTML;

    let s  = result.split("")
    s.pop()
    s = s.join("")

    document.getElementById("txt").innerHTML = s;
}

function space()
{
    let result = document.getElementById("txt").innerHTML;

    result += " "

    document.getElementById("txt").innerHTML = result;
}

